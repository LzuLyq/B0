RunAction::RunAction()
{
  // add new units for dose
  //
  const G4double milligray = 1.e-3*gray;
  const G4double microgray = 1.e-6*gray;
  const G4double nanogray  = 1.e-9*gray;
  const G4double picogray  = 1.e-12*gray;

  new G4UnitDefinition("milligray", "milliGy" , "Dose", milligray);
  new G4UnitDefinition("microgray", "microGy" , "Dose", microgray);
  new G4UnitDefinition("nanogray" , "nanoGy"  , "Dose", nanogray);
  new G4UnitDefinition("picogray" , "picoGy"  , "Dose", picogray);

  // Register accumulable to the accumulable manager
  G4AccumulableManager* accumulableManager = G4AccumulableManager::Instance();
  accumulableManager->RegisterAccumulable(fEdep);
  accumulableManager->RegisterAccumulable(fEdep2);
 
  // 分析管理器初始化和直方图创建
  auto analysisManager = G4AnalysisManager::Instance();
    analysisManager->SetVerboseLevel(1);
    analysisManager->SetNtupleMerging(true); // 多线程时合并数据

  // 创建能谱直方图：300 bins, 范围 0–3 keV
    analysisManager->CreateH1("Edep", "Energy deposition in Si detector",
                              3000, 0., 30);

    analysisManager->SetDefaultFileType("root");
    analysisManager->OpenFile("/home/sun/Geant4/geant4-v11.1.0-install/share/Geant4/examples/basic/B0/outputs/SiDetectorEdep.root");

}

